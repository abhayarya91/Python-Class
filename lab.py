import numpy as np
array1 = np.array([[1,2,3],[4,5,6]])
array2 = np.array([[7,8,9],[10,11,12]])
print("Arrya No:1...")
print(array1)
print("Array No:2..")
print(array2)

print("Addition of Array:")
print(np.add(array1,array2))

print("Subtraction of array:")
print(np.subtract(array1,array2))

print("multiplication of Array:")
print(np.multiply(array1,array2))

print("division of array:")
print(np.divide(array1,array2))

print("Square root of array1:")
print(np.square(array1))

print("Square root of array2:")
print(np.square(array2))